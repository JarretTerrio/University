public class Country_cap {
	
	private String capitol;
	private String country;
	
	public Country_cap(String coun, String cap){
		this.capitol = cap;
		this.country = coun;
	}
	
	//getters
	public String getCountry(){return country;}
	public String getCapitol(){return capitol;}
	
}
